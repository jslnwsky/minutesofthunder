	  <p>Cash burning a hole in your pocket, or simply interested in expanding your horizons? Then consider the world of RC cars, or any type of radio control vehicle. This encompasses any vehicle that you can think of -including planes, helicopters, submarines and boats; yet this usually means cars or trucks, which are the most popular vehicles in the RC world. For this you will want to look into RC car kits.<br><br>

RC car kits come in not only different car models and for different types of racing, but in different levels of performance and quality. A word to the wise is to begin at a lower level and as interest in the hobby expands, purchase better quality cars accordingly.<br><br>

Of course, RC cars can be purchased already assembled. This will usually cost more money, however, and reduces the pleasure factor for some people. As well, it diminishes the owner's desire and ability to make upgrades as time goes by and making upgrades (called tweaking) is half the fun. For these reasons, most people choose to purchase RC car kits.<br><br>

Standard RC car kits can be purchased for a price anywhere between $150 and $300. As with all things, you get what you pay for. However, there are good deals out there, so shop around and you may get a worthy bargain. As well, your search may go beyond the environs of the general hobby enthusiast store. Other venues for finding these kits include newspapers, RC magazines, and even auctions. Of course the greatest source for RC cars -whether new or used, retail or wholesale, is the internet. EBay, for example, is a wonderful venue for used RC cars. Clicking on the featured links at the top of or the Amazon.com links at the bottom of this page are great places to start your search.<br><br>

Of course, as mentioned before, there are cheaper prices to be had, for rc cars of a lesser quality. If your intention is merely to play around in your neighborhood, possibly racing your friend's RC car, then this may be the way to go. Otherwise, consider the heftier investment, particularly if you like to tinker about in the garage, which is a wonderful added bonus to this exciting hobby.<br><br></p>
