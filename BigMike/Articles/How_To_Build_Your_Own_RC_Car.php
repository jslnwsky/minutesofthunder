	  <p>Ever turn a corner and find one of those nifty little remote control cars speeding down the middle of the road? They go pretty fast and they make that neat bees-on-steroids buzzing sound. You know, you can buy one of those ready to go, but in order to get the most enjoy you really have to learn how to build your own RC car before hitting the road with it. Half the fun of the whole RC car subculture comes from knowing that you built it yourself.<br><br>

Knowing how to build your own RC car may sound like something you should take a course in, but it's really not all that difficult. Think you're going to need one of those shiny red toolboxes you see on all those TV shows about building things? Nah. In fact, once you know how to build your own RC car you may look at all things you'd like to try building and decide to do away with half the tools you own. Here's what you absolutely have to have: a couple of Phillips screwdrivers of various sizes, a few flat blade screwdrivers, including a smallish set like you would use to tighten your eyeglasses; pliers, of course, including slip-joint and needle-nose varieties. Needle-nose, that sounds like a supervillain's name, doesn't it? Speaking of villains, you'll knives, blades, the sharp stuff. You'll be working with lines that need a nice clean cut so make sure the blade is sharp and honed and, hey, be careful out there.<br><br>

Why not just go and buy one that's already street legal? Because if you know how to build your own RC car, you'll know better how to race your own RC car. Each miniature racer has its own idiosyncrasies, just like the big ones. And they break down, just like the big ones. If you already know how to build your own RC car, then you'll probably be better prepared to repair your own RC car. And if you decide to get out and race it, nothing is worse than having a car that could easily have been fixed in a half hour if only you had known how.<br><br>

Not to mention that you'll save money. The ready to run models can cost serious cash, at least compared to the take home kits. And repairing them yourself can save you even more money. If you know what you're doing, in fact, you can salvage a perfectly good car that you might otherwise have trashed. So now that you've almost decided to build it yourself, you only need to know what to really expect in the process, right? A lot depends on how you approach it. There's not nearly enough room here to run down the process step by step. But you will definitely want a step by step process to guide you. So when it comes to the process of how to build your own RC car, there's really only one piece of advice you need to know. The one piece of advice that will come far handier than anything else you may read on any web site.<br><br>

Read the instructions that come with the kit before you begin.<br><br>

