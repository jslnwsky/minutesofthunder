						All types of racing cars have different types of motors. Some have single or some have double. It is really confusing to try to sort out the difference between a single our double motor car. Plus a newbie into the world of remote control cars gets easily disoriented with what is a 13 turn double or a 13 turn single…?<br><br>

Well, in plain speaking language it refers to number of times the wires are wrapped around the armature. A 13 turn double would be 2 strands wrapped 13 times. A 13 turn single would be 1 wire wrapped 13 times. The double wires have more torque where as single wires have more speed. In general, it means that more turns means more torque but less speed and vice versa. That’s lower turns means lower torque and greater RPM. The number of winds describes how abruptly the motor accelerates. Fewer winds would mean less abrupt acceleration and more winds would lead to smoother accelerations.<br><br>

Torque basically refers to how much a force is acting on motor that causes motor to rotate. If there is more than one force acting on a motor then each of these forces shall apply torque. The net torque acting upon the motor shall be the sum total of all the forces or torques acting upon a motor. It is usually required for cars racing on the twisted tracks.<br><br>

Remote control cars come with two kinds of motors: the ones with brush and ones without i.e. Brushless. The brushless motor has windings located around the outside of the motor and the magnets are located on the moving motor shaft. A brushless motor does not provide any friction therefore provides higher RPM. But a brushless requires a lot of processing power that’s why it has not been a great success with RC trucks or cars.<br><br>

The slot less and brushless motors are third generation motors. With slot less design, motor can achieve higher efficiency, torque, fast response time and longer life time with greater with zero maintenance. Cogging problem is also eliminated even at lower speed operation. But since there is a lot of research and development involved in their development they come out as expensive solution in case of RC cars.<br><br>

There are also sensor less and motors with sensors. Sensor motors are older technology with less cogging, less rpm and less power. On the other hand sensor less motors are newer technology with more power and more rpm. Both have difference of cogging on start up. This can prove to be a little troublesome when you are in a race. It seems like forever for a sensor motor to start up. But sensor less motor gives smoother throttle response, efficient running and a fewer wires to mess up with.<br><br>

Remote control cars have a lot to do with the motors and their functioning. When preparing for a race, checking out on motors can be really helpful. If your car is heavy then may be a motor with more torque would be required. A speedy light action racer would need a greater rpm. Connections and wires to all the motors need to be properly fixed and checked. If you have some cash to spare for the race then going in for the slot less motor can be good idea.<br><br> 
 
