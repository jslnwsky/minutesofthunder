					<p>
						<p>The roar of the crowd as the cars rush by, the sense of excitement in the air. Is it the Indy 500? No, it is RC racing! RC racing, or remote control racing also known as radio control, is a quickly growing hobby segment that appeals to both young and old, and men and women.</p>

<p>RC cars are popular at a hobby level, for creating and putting the cars together, or even taking it to the next level with professional tuning and racing. You can purchase RC cars as a kit in an unassembled form or completely assembled and rtr or ready to run condition.</p>

<p>What Types of RC Cars Are There?</p>

<p>The selection for RC cars is huge and growing every day. You can choose cars in almost any body type that you desire, in any color and any style. However, there are a few main distinctions that you will need to make before you choose any car for the first time.</p>

<p>You can choose from:</p>

<p>1) Gas powered or electric<br>
2) On road racing models or off-road models<br>
3) Assembled or non-assembled<br>
4) Car or Truck</p>

<p>These are the main choices, however once you choose what type you are looking for, you will still have many style choices as well as engine choices to make.</p>

<p>Gas Powered or Electric?</p>

<p>The biggest decision that will have to make when you choose an RC car is whether it is powered by nitro gasoline or an electric battery pack. There are pros and cons to both types, and ultimately you need to decide what type of power is best for your needs and what you want your RC car to achieve.</p>

<p>Electric RC Cars</p>

<p>Electric remote control cars are an excellent choice if you are just getting started in the world of RC cars. They are typically easier to care for, and much easier to maintain than their gas powered counter parts. An electric car will make a great starter car because they are generally less expensive than a nitro car, so you can take the time to practice and perfect your driving skills before laying down a lot of money.</p>

<p>Electric cars are run on batteries. The batteries are assembled into battery packs that usually have a recharging capability. This is much more cost effective than cars that require new batteries. However it can also be time consuming having to recharge your battery pack, so if you do not like to wait, a spare battery pack is likely a good investment.</p>

<p>You can purchase electric RC cars for any budget, so you should choose a car that is suitable for your needs. Electric cars typically go slower than a nitro car, however you can purchase electric cars that will achieve high speeds.</p>

<p>It is important to remember when you buy a new electric car you must break in the motor slowly in order to extend the motor life and allow the engine to achieve its full potential. When you break in the motor, you simply start running the car at a slower speed and gradually bring it up to speed. If you do not want to take the time to break in an engine you can purchase cars ready to run. In most cases, these cars are ready to go and the motors are already broken in.</p>

<p>Nitro Gas Cars</p>

<p>RC cars that run on nitro gasoline are the most popular and fastest growing segment of remote control cars. With nitro vehicles, you can buy the popular trucks that will take jumps at high speeds and go off road driving. Alternatively, if speed is more your thing, look for the racecars that are meant to speed along pavement, tracks or other prepared surfaces.</p>

<p>As with electric RC cars, you can purchase ready to run cars or kits in varying states of assembly. Nitro RC vehicles typically run faster than electric vehicles and you can find some that will go 70mph right out of the box.</p>

<p>Gas powered RC vehicles run on a specially formulated fuel made especially for RC cars. Most nitro vehicles have a pull start engine similar to a lawnmower for easy starting. Because nitro cars are more complex than electric vehicles, it is important that you follow the instructions carefully. You can purchase nitro starter kits that come with everything that you need to get started. This is a great way to go for beginners when you do not want to leave anything out.</p>

<p>As with electric cars, it is very important to break in your engine unless of course you purchase a ready to run kit, which should be ready to go straight out of the box.</p>

<p>Which Way to Go</p>

<p>No matter if you choose the nitro gas powered version or the battery powered RC vehicles, you will have a great selection available to you, and of course, you will have a lot of fun. Why not try a few of each? With so much variety out there, it is almost impossible to settle on only one RC vehicle.</p>

