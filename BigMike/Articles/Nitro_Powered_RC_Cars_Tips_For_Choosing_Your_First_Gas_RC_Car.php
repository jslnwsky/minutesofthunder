	  <p>RC (radio controlled) cars, especially the nitro or gas powered RC cars, are becoming increasingly popular. With speeds up to 70 mph, realistic looks, and racing clubs in virtually every large city, it's easy to see why.<br><br>

If you want to join this exciting hobby, there're a few things you should consider before you buy your first nitro-powered RC car. The basic considerations are: size, type, 2 or 4-stroke motor, maintenance, 2 or 4 wheel drive, and ready-to-run (rtr) or kit cars.<br><br>

Sizes<br><br>

The two most popular sizes to choose from are 1/8 and 1/10 scale. 1/10th scale is the industry standard for on-road racers, while 1/8th is more popular for off-road trucks and buggies. The larger 1/8th scale on road car comes standard with a 2 or 3 speed automatic transmission.<br><br>

Types<br><br>

The touring and racing cars are the popular choice for on-road use. For best performance, they should be run on a smooth surface.<br><br>

Trucks and buggies are the choice if off-road action is what you want. Though not as fast as the touring and racing styles, they are still very impressive and extremely rugged as well. And since a smooth surface is not required, they also have the advantage of being able to run just about anywhere.<br><br>

Motors<br><br>

Nitro powered RC motors come available in the popular 2-stroke or the less conventional 4- stroke versions. The primary difference is that the 2-stroke motor, much like a weed eater or chain saw, requires a fuel oil mixture. The 4- stroke motor has an oil reservoir and can run on straight fuel. The 2 stroke engine has the advantage of producing higher rpm's (revs up faster) and is more suitable for racing. The 4 stroke engine has more power and torque and is better for offroad use.<br><br>

The most popular 2-stroke motor is the 23cc (cubic centimeter) displacement engine. It's popularity is due to the amazing 2.5 HP of output it produces. The resulting high speeds and acceleration are what RC racers love.<br><br>

Additionally, motors come with or without a pull start. The ones without a pull start are cheaper, but you'll also need a starter box.<br><br>

Maintenance for Nitro Powered RC Cars<br><br>

Maintenance is a definite requirement of running a nitro powered vehicle. Most hobbyist love tweaking and tuning their vehicles. In addition, you'll need to maintain certain parts such as:<br><br>

Clutch<br>
Differential<br>
Air Filter<br>
Header and<br>
Pull start cord<br>

2 or 4 Wheel Drive<br><br>

If you're new to the hobby, you'll find a 2wd car less expensive and easier to work on. The 4wd car has the advantage of better traction and handling in turns which makes it a better choice when you're ready to race.<br><br>

Kits or Ready to Run (RTR) Rc Cars<br><br>

Nitro powered rc cars come in kits or ready-to-run right from the box. The primary difference is the whether you want to save time with a RTR car or save money with a kit. However, because of the assembly process, kits better prepare you for required maintenance.<br><br>

If you choose to build an rc car, don't expect to finish in one sitting. To avoid mistakes, familiarize yourself with the instructions first and get your work area prepared. Some of the things you'll need are:<br><br>

Small No. 1 and 2 Phillips and flathead screwdrivers<br>
Soap - as a dry lubricant for tight parts<br>
Extra fuel line - to hold screws while positioning<br>
Needle nose and regular slip joint pliers<br>
Flush cutter<br>
Hobby knife with no. 11 blades<br><br>

When you assemble the car, make sure to work in a well-lit, uncluttered area. You should keep the parts and tools separated using tin boxes, trays, or even an old fishing tackle box.<br><br>

These are a few of the basics you'll need to know before you buy your first nitro rc car or truck. You should expect to pay around $400 for a complete beginner setup. The price will vary a little depending on whether you choose a kit or RTR and how many tools you need.<br><br>

Whether you race or just practice by yourself, get ready for a lot of fun!<br><br>

