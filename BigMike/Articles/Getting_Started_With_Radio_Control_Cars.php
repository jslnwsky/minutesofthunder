	  <p>Take note that you need to decide whether you want a radio control nitro or gas
car or perhaps one that has an electric engine. You could purchase either one
which is ready to run (rtr) or a kit. Now, knowing how much you would want to
spend start going shopping for a vehicle that fits your specifications. Like me
passion is what drew me to this exciting hobby and there's a lot you are going
to learn before you can get behind the wheel.<br><br>

It is always good start at a nearby hobby store - look for one that specializes in radio control vehicles. Speak to people you'll find working in the store and chances are that these people are true blue rc hobbyists as well. There is much you can learn from them, so pick their brains well. You'll find these machines to be sleek and powerful, tearing up the terrain in a jiffy and it's certainly a lot of fun watching them go.<br><br>

The types of radio control vehicles that you need to get aquainted with are nitro and electric cars, monster trucks, stadium trucks, minis and micros. If you want to get your feet wet and don't wish to spend a whole lot of money, go and get a used vehicle - this is the cheapest way of building your skills. To find used radio control vehicles for sale, ask your friends, check the local newspapers, read RC magazines or browse through listings at auction sites like eBay.<br><br>

If you decide that you want a new vehicle, you can choose a kit, an ARR (almost ready to run) model, or an RTR (ready to run) model. What's the difference? A kit will take you time and a certain amount of expertise to assemble. The advantage of a kit, of course is that once you're finished, you'll know exactly how your vehicle is built and therefore how to repair any damage.<br><br>

ARR and RTR models come already assembled. The difference is usually that the ARR model doesn't have a radio system installed, while an RTR model does. So don't be fooled by the difference in price. When you buy an ARR car, you're going to have to add the cost of the radio system to your final cost.<br><br>

The expense of operating your radio control car can be as little as an occasional replacement battery pack. But as with any hobby, the more you play the more you pay. If you become a dedicated radio control vehicle fan, be prepared for repairs, upgrades, special tools, magazine subscriptions, books, racing entry fees, association dues, travel expenses to the big meets and races - the list can be endless. The best advice is to start small, and let your radio control hobby grow little by little to whatever level you find most satisfying. <br><br>

So all in all, what's the best advice for getting started in radio control? Start small, get to know experienced radio control drivers, and learn as you go. Make a list before you buy anything, and make sure have all the accessories and parts you're going to need to use your vehicle. Most of all, have fun - RC is a wonderful way to meet people, to challenge yourself, and to fulfill your driving fantasies.<br><br>

