					<p>
						If you are thinking of buying your first remote control truck, here are some things you need to keep in mind. I would advise you pick yourself up an RTR (ready to run) kit to begin with. These RTR RC trucks often come fully assembled, or require only minimal assembly like gluing the tires to the wheels. Most of these models now also include a 2 channel radio and the servos pre-installed, saving you the hassle of trying to match a radio to servos.<br><br>

Ready to run kits may be slightly more expensive than their basic unassembled kit counterparts, but if you do not have the building experience, it will save you weeks of work in assembly time, frustration and errors.<br><br>

Imagine being able to bring home, or have delivered to your door, your brand spanking new RC nitro truck, removing it from it’s packaging, inserting some batteries and filling with nitro fuel…..and being able to hit the dirt track straight off! This is the beauty of RTR RC truck kits.<br><br>

Some more experienced racers, or even some very adventurous newcomers, may want to build their RC monster truck from scratch, so that they get to know all the components and how they are assembled. But even with a RTR model, you will gain this knowledge anyhow by maintaining, modifying and tweaking your truck once you start to use it. If you do plan to build your own truck then allow yourself 3-5 days to put it together.<br><br>

You can expect to pay $50 - $100 more for a ready to run RC nitro truck and you will probably be looking at paying $300 - $500 for a good level reliable RC truck.<br><br>

To fully get going right off the rails, you will need some extras which you should remember to add to your order, or you won’t get your truck out on that first day! You will obviously need some nitro fuel and a fuel bottle is a better option for refuelling to avoid any fuel spillage. As well as this you will need a glow plug heater and you may need an electric starter pack if your truck does not have a pull cord start. It is also recommended to have some oil for routine lubrication maintenance after its first run. All in all these extras should not cost you any more than $50 - $100.<br><br>

Most major manufacturers now produce fully ready to run monster trucks including Traxxas, CEN, Tamiya and Team Associated.<br><br>

Yes, this can be an expensive hobby, but weigh this up against the adrenalin pumping action of these RC monster trucks and there is no contest! After all – it’s only money!<br><br>

