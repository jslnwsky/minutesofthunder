	  <p><p>RC Cars offer all the excitement of motor racing at a fraction of the cost. They also offer a safe form of motor sport and allow youngsters to begin to understand car control at a very young age.<br><br>There are various types of RC cars that are suitable for racing, but the two main types are the gas-powered cars and the electric powered cars.<br><br>They both offer different benefits, and there will always be those who believe one form of motivation is better than the other.<br><br>The gas-powered cars are generally a lot more powerful and offer the ultimate in performance but they are a lot noisier and the motors do require tuning. Gas powered cars can sometimes be quite temperamental to tune and require some degree of knowledge to extract their ultimate performance.<br><br>The electric powered cars on the other hand are relatively simple to race and maintain, but they are limited by the charge in their batteries.<br><br>Electric powered RC cars are a lot quieter than gas powered cars and this might affect your decision if you are planning on using them in the local neighbourhood and want to keep your neighbours as friends.<br><br>Most of the top end RC cars offer all the suspension, gearbox and differential tuning as a full size race car, so the beginner RC car racer might feel a little overwhelmed by the amount of tuning that is required to get the best out of their race car.<br><br>Like anything else, it is a process of learning and RC car owners will soon find that trying different race settings can make a huge difference to the performance of the racing car.<br>Once familiar with the changes that can be made to the car, the owner will be happy that these variables have been built into the race car, offering those with the most testing and knowledge to have a decided advantage come race day.<br><br>It would be wise to look at the race facilities in your local area and talk to those who are racing to determine what type of vehicle you can race before 