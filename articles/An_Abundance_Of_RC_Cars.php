	  <p>Ever been walking down the street and heard a high-pitched whirring noise? It's so high pitched that you know it can't be a car or motorcycle -and then it happens. Suddenly a small remote controlled automobile zips by you. Or maybe the owner has a little fun with you and the little buggy goes around you a few times. Welcome to the world of RC cars.<br><br>

Remote or radio -controlled vehicles are not only on the upswing in occurrence, but in overall popularity. Today it seems that everyone knows at least someone into RC cars. The abundance of different types of such toys is staggering as well. Only, don't call them toys to a true enthusiast as some of them will get upset with you and call them a passion, or some other such phrase. But hey, can't a Porsche or Ferrari -the real-life model, that is, also be a toy of sorts?<br><br>

Whatever you call them, it's worth getting a closer look at these stimulating toys of passion and their many types. For the purposes of this article, the term RC cars and similar terms will effectively signify vehicles of all shapes that are controlled by radio.<br><br>

Of course, there is the standard car. This and all remote controlled vehicles come in numerous sizes, some of which can even carry a small child, though rarely much larger. This is your standard four-wheeled car, truck, minivan, SUV, Humvee (which comes in both military and civilian markings), sixteen wheelers, motorcycles, and other such modes of transportation.<br><br>

Every color and type of marking imaginable is also existent. Some models are specifically aimed at males, whereas others are gender specific to girls of varying ages. Different ages are important, as a great number of RC cars are owned by people over the age of eighteen.<br><br>

By their nature, RC vehicles can dip into the imaginative and even bizarre. This means that radio controlled Batmobiles and other such comic fare exist, along with moving bugs, such as beatles, and even 3 and 4-wheeled fantasy cars that jump, spin, and sometimes can flip over themselves. Soon, even the sky won't be the limit.<br><br></p>
