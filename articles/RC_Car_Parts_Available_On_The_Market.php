	  <p>Racing enthusiasts seem to be everywhere today. Whether it's the racing of turtles, camels, cars, or even model vehicles, the heat is on for this expanding sport. One of the top versions of racing is that of radio controlled, or RC vehicles. Yet inevitably, as in all forms of mechanical racing, parts break or simply wear down from the forces of entropy. For this reason, there is a steady demand for RC car parts.<br><br>

RC car parts come in every size and shape that is initially made for the completed models and generally implies everything but the frame of the model car itself. For the purposes of this article, some of the more common, as well as colorful, RC car parts will be discussed.<br><br>

First there are the engines. These come in a multitude of sizes and speeds, depending upon the car. As well, the manifolds usually allow for high air flow in single or dual chambers apt for racing. Moreover, what RC parts kit (which usually includes numerous parts) exists without a transmission, many of which are adjustable.<br><br>

Of course, wherever any model part is concerned, there is usually a RC car parts model that echoes its larger version. So it is with brakes -often disc brakes, just as with true automobiles. This is seconded by a suspension that is designed to give a radio controlled vehicle a longer life as well as to give it 'jumpier' movability, along with a strong yet flexible drive shaft.<br><br>

Whenever a vehicle is concerned, it goes without saying that a fuel source is necessary and so for a RC model, so too are fuel tanks. Often, a singular tank can be adapted to piggyback a second tank, thus giving a model more road time before refueling. To add to this, a fuel pump is also available, along with carburetors, naturally.<br><br>

Finally, there are the radio and receiver. The former is much more expensive as a sole replacement part, yet the receiver is not so costly and generally is much easier to locate. All of these parts are available via the internet as well as at local merchants specializing in this market.</p>
