        <br />
Electric RC <b>cars</b> and trucks are generally considered best for beginners, since even if you choose to build your own car, they tend to be simpler and easier than nitro cars.  They're also a great deal quieter and run much cleaner, meaning you're less restricted by where you can run them.  In terms of speed and power, they do have a great deal of pickup, though not as much as the nitro cars.<br />
<br />
Electric RC <b>cars</b> use rechargeable battery packs to power their motor and steering, which are usually recharged from a 12-volt car battery or wall socket.  Batteries run for about 5-10 minutes, depending on the type of engine your car has, and charging the battery usually takes 15-30 minutes.  Because of this, it is strongly recommended you have at least two battery packs, to allow for quick replacement of the battery.  This means your car can keep running while the other battery is recharging, giving the car more overall running time.<br />
<br />
At first glance, getting started with an electric RC car can be much less expensive than a nitro vehicle. But there are other costs to consider as well, such as additional battery packs, a battery charger and other accessories that will add to the cost, making it closer to the price of a nitro car in the long run.  Of course, this cost also depends on what kind of car you end up purchasing and what kind of battery pack it requires, as well as how often you run the car and the quality of the batteries you get.  Though the initial outlay of cash can be steep, but you'll want to get quality battery packs and a good charger to save replacing cheaper batteries.<br />
<br />
The main reason electric RC's are said to be easier than nitro is in the amount of maintenance and tuning their engines require.  Though the care, maintenance and cost of battery packs is steep, it is still less trouble for the new driver than the air filters, tuning, fueling and various other engine parts that require attention on a nitro car.  Instead, careful conditioning and proper storage of your battery packs will keep your electric RC running smoothly for years.  Always consult your manufacturer's instructions to make sure you're getting the right battery packs for your car, and that you're caring for them properly.<br />
<br />
Easier and cleaner, electric RC <b>cars</b> and trucks offer the genuine racing experience to the beginner on an easy learning curve.  Proper conditioning and maintenance of the car and its battery packs are still easier than the many parts and problems often associated with nitro RC's.  If you're a beginner, or if you just want to get to the races, an electric RC can offer you the speed and <b>fun</b> you're after for less work.  <br />
<br />
Also keep in mind that if you think you'd prefer an electric RC, but still want the experience of building your own car, that you can also purchase electric kits.  These include complete instructions to build your own car from scratch, and because their systems are less complex than the nitro cars, they are a little easier to build yourself.<br />
<br />
Electric RC Car Motors<br />
<br />
In order to prevent unnecessary wear and tear on your electric motor, it is important to always break in your motor, before you drive it for the first time, and every time after you change its brushes. One easy method is to run the vehicle with the wheels off of the ground at about 1/4 power for about 5 minutes. This will slowly get the brushes fully seated to the commutator without causing wear and tear on the engine, and will allow your motor to run at its full potential.<br />
 <br />
Your electric car will come with instructions on how to change the brushes on the motor, as well guidelines for how often.  Remember, if you change the brushes on your motor, be sure to break it in again. How often you replace the brushesand the motor, for that matterdepends on where and how much you're running or racing your car.  Generally, a motor should be replaced after it has gone through five or more pairs of brushes, but it will always depend on the individual car, its motor and how well they're running.<br />
