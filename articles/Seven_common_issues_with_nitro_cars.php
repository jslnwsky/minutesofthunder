	  <p><p><strong>Piston sticks at the top of the cylinder (new engine).</strong><BR>
	    <BR> The pistons tolerance inside the piston chamber is so tight, there is no room for the piston to slide to the top. The <BR>
	      solution is to heat up the piston chamber with a hair dryer or heat gun. This will expand the chamber, allowing the piston to glide smoothly, while maintaining good <BR>
	      compression.
<BR><BR>
<strong>Nitro engine dies as soon as you pull it off the starter box</strong><BR>
<BR>If your RC car is decently tuned, your problem is <BR>
probably a loose clutch nut . The clutch nut is <BR>
what connects the flywheel to the crankshaft. Pressing down <BR>
 your RC car on the starter box presses the clutch nut<br> 
against the fly wheel, creating the spin that <BR>
	    gets your nitro started. However, if your clutch nut is loose <br> 
	    from wear and tear or impacts, it won't have the proper mesh it <br>
	    needs to keep the car running once you have removed your vehicle from <BR>
	    the starter box. To see if you have a loose <BR>
	    clutch nut, look for the fly wheel spinning on <BR>
	    it's own. If it does, you need to remove the engine and tighten <BR>
	     or replace the clutch nut.<BR>
        <BR>
	    <strong>Engine stall when car put on the ground.</strong><BR>
        <BR>
       A siezed clutch-bell bearing or broken clutch-retainer <br>
       spring is the most likely culprit. The engine stalls because <br>
       the clutch is engaged when the engine is idling. When you <br>
       put the car on the ground the engine croaks as it is not <br>
       running fast enough to move the car.
        The retainer spring <br>
        or clutch bell bearing are forcing the clutch to be engaged<br>
        when it shouldn't be. You'll need to inspect both parts and<br>
        repair or replace as required.
        <BR>
	  <p><strong>Car engine running backwards. </strong>      
	  <p>This is a common problem, with an easy fix as it is always<br>
      caused by user errors. Make sure you put your RC car on the<br>
      starter box with the correct orientation and that you put your <br>
      wires on the correct poles of the battery.
      
	  <P><strong>Your engine dies while warming up.</strong>
	  <P>The most probable cause of this is a worn piston and/or cylinder sleeve.<br>
	    When the engine is cold, compression is adequate to get it started<br>
	    and run nicely. As it warms up the fit between piston and sleeve <br>
	    lossens up as the sleeve expands. 
      If the fit is bad enough, your <br>
      fuel can exit the cylinder above the piston . As the engine gets <br>
      warmer, this effect gets worse until the engine eventually stalls.<br> 
      You'll need to replace the engine or, if you like working on this <br>kind of thing, the piston and sleeve. 
	  <P> <strong>Your roto-start or pullstart nitro  won't start.<br>
	  </strong><BR>
        Your one-way bearing may not be catching on the crank shaft.<br>
        This can happen if it is extremely dirty
        and you can clean it , <br>
      let it dry overnight, and re-install it.       
	  <P><strong>Your engine speed  increases while idling.</strong><br>
	    <br>
	    Highly probable that the low speed needle on the engine is set<br>
	    too lean
	    .  A good confirmatory test is to pinch the fuel line and <br>
	    time how long the engine runs. If much less than five seconds <br>
	    it's too lean, if much longer than 5 seconds, it's too rich. 
