	  <p>Believe it or not, the most important tool for tuning and modifying RC cars, in any RC racer's pit box, probably isn't even sold at your local hobby store. Despite thier absence at hobby stores, you will see them laying on EVERY serious RC pro's pit area.<br><br>

The tool is, collectively, the pencil and paper. One of the most enjoyable experiences in RC cars, besides buying a new trick ride, is testing something new, in the hope you will discover the slight edge, or even a HUGE edge. <br><br>

Today's RC cars, buggies, and trucks have so many variables that can be tinkered with, even the slightest adjustment can have a drastic effect on the handling and speed of your ride. Whether you race or just bash, you really should write down the current operating state of your RC ride. Variables such as ride hieght, camber, caster, the toe-in, the fuel mixture you use, the charge time of your batteries, the needle settings of your nitro, the running temp, how many races you get in before you need to rebuild your motor, the shock oil you use, and any thing else that you can think of.<br><br>

As I've shared with the subscribers to my RC ezine, you can't improve what you don't measure. If you are serious about racing RC cars, you not only need to master your hand eye coordination, but you should become compatent at adjusting your RC car to the conditions you race under. Your RC car performs differently on asphalt at 10 a.m., with a certain compound tire, than it will at 5 p.m., after the oil and moisture has heated up from exposure from the sun. The traction can vary greatly. And with all the excitement and preparation of the day's events, writing down these changes can be of great value for when you prepare your chassis or tire choice for the following week. If you didn't write them down, you will probably forget them.<br><br>

And you weekend bashers might be thinking that you aren't serious at RC, so it's not necesary. But let me tell you, if you take the time to write down the current state of your RC cars and trucks, when you eventually change something, or fate throws a tree in your way and adjusts your car setup for you, you will be able to measure your setup and get back to that original state, with no guess work. That means less down time, and more bashing. And if you are still at a loss on how to get back to that original state, you've got it in writing for someone else, like the local hobby store guy, to look over.<br><br>

I encourage everyone of you to write down your car's setup info. It's not only useful, but it will multiply how much fun you get outta your RC cars, trucks, and buggies. Heck, this little tip works on real life stuff as well.<br><br>

There is one more use for this very important RC tool, and that is to write down your lap times. In order to improve and gauge your racing level, you need to be aware of your lap times. Most racing clubs can print out a copy of the days races for you or post them to thier club website, but to really measure your success, you should keep a log of how you do from week to week. If you do, I promise you, you will find a whole new appreciation of the RC cars hobby. You will find your mind focusing and aware of ways to improve, such as cornering better, or controlling your throttle just right. It really comes down to the way the human mind works. When it focuses on a goal, it tends to make make strides in that direction subconsciously. So take my advice... write it down and measure.<br><br>

