	  <p>There is no better time than today to get involved with RC, or radio controlled vehicles. This is as they are readily available for building or already built, there are numerous informative and fun magazines specifically dedicated to this hobby, and by way of the internet, RC car bodies and other accessories are easily purchased at economical prices. This article will focus upon the numerous vehicle bodies that can be purchased.<br><br>

Naturally enough, there are numerous types of RC car bodies available. At the top of the list are nitro cars (meaning that they are gas powered), electric cars, mini vehicles, and an assortment of non car type vehicles. These include tanks, SUV's, jeeps, helicopters, and boats.<br><br>

Furthermore, when purchasing RC car bodies, there is always an additional option. This is to buy a package deal. For the nitro kits, this usually means the addition of starter kits, igniters, extra fuel, a fuel bottle, tools, and batteries. The package deal for the electric car bodies is similar, though minus the fuel.<br><br>

Now let's discuss some of the RC car bodies that are available on the market to get an idea of what is available. The Subaru Impreza CT5 is a sedan whose latest upgrade runs at nearly 74 miles per hour! Guaranteed to be reliable, it's also a sure bet towards great fun.<br><br>

For an even sleeker race look, consider the Nitro RS4 with a Viper body. This is a race car scaled down to 1/10 of the actual vehicle. While it's not as fast as the Impreza, at 35 mph, it will still rev your gears. As well, its four wheel drive makes for more terrain accessibility.<br><br>

When it comes to racecar type bodies, the racers are strangely slower than other models. This is not so with the Traxxas 4-Tec RTR however. At 60 mph, it's TRX 2.5 engine can stay with the best as it's designed specifically for racing.<br><br>

Looking more for a multi-terrain road dog? If this is so, then the Traxxas Rustler is worth checking out! One of the fastest vehicles of its class, it has been recorded at speeds exceeding 47 mph and is perfect for mud flats and desert floor racing.<br><br>

This is but a small sample of the RC bodies available to the avid racer. There are different models for different races, enough to satisfy anyone with the racing spirit in him or her.</p>
