	  <p>Today remote controlled vehicle enthusiasm has begun to become more visible than ever. Cars and trucks run by remote can be seen tearing down the road amid many cheers from the owners of such toys, as well as an occasional passerby or two. Yet, the fact is that the roads are illegal and for this many enthusiasts are now looking at RC car tracks. This article will discuss this specific topic.<br><br>

RC car tracks refer to two different entities, which should be defined. The first of the RC car tracks is the portable type. This is the track set which is detachable and great for kids. The great aspect to this, besides the fact that a remote controlled vehicle can speed along at high speeds is that the track can be usually be modified to different sizes and shapes. This includes loops which allow the cars to do somersaults and flips through the air. Additionally, there are numerous fun aspects to these detachable tracks which can be used or omitted. These include street signs, lights, bridges, flags, struts, and other such amenities<br><br>

The second type of RC car tracks is the physical track; that is, the actual dirt mini-roads that remotely controlled cars race on. In the world of RC vehicles these tracks can be as wild and difficult as the tracks used by motorcycle racers. In fact, due to the downscaled size, they can be more complex when the effort is contributed to such ideas. Thus, just as an instance, roads can be extremely well defined, or shoddy, rocky, and difficult to tread. As well, they can be wide and maneuverable, or thin and difficult to maneuver, thus challenging controllers.<br><br>

Some of the features of a true RC track include cement or dirt racing floor, mud, puddles and other water distractions, greens, trees, and of course massive hills so the vehicles can catch air. To be sure, this is only the beginning to what can be added to such tracks. Truly, small cities can be added to provide an urban track, country road tracks and everything in between to make for an exciting visual display whilst at the same time challenging the RC vehicles and their operators.</p>
